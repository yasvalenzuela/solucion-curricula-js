var orderFood = function(menu) {
	if(menu.indexof("Ceviche") >=0){
		return "Ceviche";

	} else if(menu.indexof("Tacos al pastor" ) >=0){
    	return "Tacos al pastor";

    } if(menu.indexof("Empanada chilena") >=0) {
    	return "Empanda chilena";

    }if(menu.indexof("Hamburguesa") >=0){
    	return "Hamburguesa";
    }
}
